import { formatResponseForPrint } from "@/lib/format-response";

export function downloadTextFile(content: string, fileName: string) {
  const blob = new Blob([content], { type: "text/plain;charset=utf-8" });
  const url = URL.createObjectURL(blob);
  const link = document.createElement("a");
  link.href = url;
  link.download = fileName.endsWith(".txt") ? fileName : `${fileName}.txt`;
  link.click();
  URL.revokeObjectURL(url);
}

export function printResult(options: {
  title: string;
  content: string;
  subtitle?: string;
}) {
  const bodyHtml = formatResponseForPrint(options.content);
  const html = `<!DOCTYPE html>
<html lang="en">
<head>
  <meta charset="utf-8" />
  <title>${escapeHtml(options.title)}</title>
  <style>
    body { font-family: Georgia, "Times New Roman", serif; color: #111; line-height: 1.6; padding: 24px; max-width: 800px; margin: 0 auto; }
    h1 { font-size: 1.5rem; margin: 0 0 0.25rem; }
    .subtitle { color: #555; font-size: 0.95rem; margin: 0 0 1.5rem; }
    .footer { margin-top: 2rem; padding-top: 1rem; border-top: 1px solid #ddd; font-size: 0.8rem; color: #666; }
    h2 { font-size: 1.2rem; margin-top: 1.25rem; }
    h3 { font-size: 1.05rem; margin-top: 1rem; }
    p { margin: 0.5rem 0; }
    strong { font-weight: 700; }
    @media print {
      body { padding: 0; }
    }
  </style>
</head>
<body>
  <h1>${escapeHtml(options.title)}</h1>
  ${options.subtitle ? `<p class="subtitle">${escapeHtml(options.subtitle)}</p>` : ""}
  <div>${bodyHtml}</div>
  <p class="footer">Generated by School Homework Helper</p>
</body>
</html>`;

  const iframe = document.createElement("iframe");
  iframe.setAttribute("title", "Print quiz");
  iframe.style.position = "fixed";
  iframe.style.right = "0";
  iframe.style.bottom = "0";
  iframe.style.width = "0";
  iframe.style.height = "0";
  iframe.style.border = "0";
  iframe.style.visibility = "hidden";
  document.body.appendChild(iframe);

  const frameWindow = iframe.contentWindow;
  const frameDoc = frameWindow?.document;
  if (!frameWindow || !frameDoc) {
    document.body.removeChild(iframe);
    return false;
  }

  frameDoc.open();
  frameDoc.write(html);
  frameDoc.close();

  const triggerPrint = () => {
    frameWindow.focus();
    frameWindow.print();
    window.setTimeout(() => {
      if (iframe.parentNode) {
        document.body.removeChild(iframe);
      }
    }, 1000);
  };

  if (frameDoc.readyState === "complete") {
    window.setTimeout(triggerPrint, 100);
  } else {
    iframe.onload = () => window.setTimeout(triggerPrint, 100);
  }

  return true;
}

function escapeHtml(text: string) {
  return text
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;");
}

export function slugifyFileName(text: string) {
  return text.toLowerCase().replace(/[^a-z0-9]+/g, "-").replace(/^-|-$/g, "") || "quiz";
}
